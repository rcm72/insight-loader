CREATE TABLE neoj_invoice_items (
    invoice_item_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    invoice_id      NUMBER NOT NULL,
    product_id      NUMBER NOT NULL,
    quantity        NUMBER,
    unit_price      NUMBER(10,2),
    FOREIGN KEY (invoice_id) REFERENCES neoj_invoices(invoice_id),
    FOREIGN KEY (product_id) REFERENCES neoj_products(product_id)
);

CREATE SEQUENCE neoj_order_seq START WITH 1 INCREMENT BY 1;

