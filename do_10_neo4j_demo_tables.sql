CREATE TABLE neoj_invoices (
    invoice_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id     NUMBER NOT NULL,
    created_at   DATE DEFAULT SYSDATE,
    FOREIGN KEY (order_id) REFERENCES neoj_orders(order_id)
);

